package com.eam.android.model;

public class EamShortItem {

	private String eamOriginator;
	private String eamSubject;
	private String eamMessageDate;
	private String eamType;
	public String getEamOriginator() {
		return eamOriginator;
	}
	public void setEamOriginator(String eamOriginator) {
		this.eamOriginator = eamOriginator;
	}
	public String getEamSubject() {
		return eamSubject;
	}
	public void setEamSubject(String eamSubject) {
		this.eamSubject = eamSubject;
	}
	public String getEamMessageDate() {
		return eamMessageDate;
	}
	public void setEamMessageDate(String eamMessageDate) {
		this.eamMessageDate = eamMessageDate;
	}
	public String getEamType() {
		return eamType;
	}
	public void setEamType(String eamType) {
		this.eamType = eamType;
	}	
}
